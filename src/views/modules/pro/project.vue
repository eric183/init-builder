<template>
    <div class="mod-user">
        <el-form :inline="true" :model="dataForm" @keyup.enter.native="getDataList()" size="small">
            <el-form-item label="类型:">
                <el-select v-model="dataForm.projectType" type="password" placeholder="项目类型">
                    <el-option v-for="item in projectTypelist" :key="item.value" :label="item.label" :value="item.value"></el-option>
                </el-select>
            </el-form-item>
            <el-form-item> <el-input v-model="dataForm.projectSn" placeholder="项目编号" clearable></el-input> </el-form-item>
            <el-form-item> <el-input v-model="dataForm.abbreviation" placeholder="项目简称" clearable></el-input> </el-form-item>
            <el-form-item>
                <el-button type="primary" @click="getDataList()">查询</el-button>
                <el-button type="success" @click="addOrUpdateHandle()">新增</el-button>
                <!-- <el-button v-if="isAuth('sys:user:delete')" type="danger" @click="deleteHandle()" :disabled="dataListSelections.length <= 0">批量删除</el-button> -->
            </el-form-item>
        </el-form>
        <el-table :data="dataList" border v-loading="dataListLoading" @selection-change="selectionChangeHandle" style="width: 100%;">
            <el-table-column type="index" header-align="center" align="center" width="50" label="序号"> </el-table-column>
            <el-table-column prop="projectSn" header-align="center" align="center" label="项目编号"> </el-table-column>
            <el-table-column prop="projectName" header-align="center" align="center" label="项目名称"> </el-table-column>
            <el-table-column prop="abbreviation" header-align="center" align="center" label="项目简称"> </el-table-column>
            <el-table-column prop="projectType" :formatter="formatType" header-align="center" align="center" label="项目类型"> </el-table-column>
            <el-table-column prop="province" header-align="center" align="center" label="省"> </el-table-column>
            <el-table-column prop="city" header-align="center" align="center" label="城市"> </el-table-column>
            <el-table-column prop="region" header-align="center" align="center" label="县区"> </el-table-column>
            <el-table-column prop="address" header-align="center" align="center" label="地址"> </el-table-column>
            <el-table-column prop="totalArea" header-align="center" align="center" label="建筑面积"> </el-table-column>
            <el-table-column prop="rentedArea" header-align="center" align="center" label="出租面积"> </el-table-column>
            <el-table-column header-align="center" align="center" width="150" label="操作">
                <template slot-scope="scope">
                    <el-button type="success" plain size="mini" @click="addOrUpdateHandle(scope.row.projectId)">修改</el-button>
                    <el-button type="danger" plain size="mini" @click="deleteHandle(scope.row.projectId)">删除</el-button>
                </template>
            </el-table-column>
        </el-table>
        <el-pagination
            @size-change="sizeChangeHandle"
            @current-change="currentChangeHandle"
            :current-page="pageIndex"
            :page-sizes="[10, 20, 50, 100]"
            :page-size="pageSize"
            :total="totalPage"
            layout="total, sizes, prev, pager, next, jumper"
        >
        </el-pagination>
        <!-- 弹窗, 新增 / 修改 -->
        <add-or-update v-if="addOrUpdateVisible" ref="addOrUpdate" @refreshDataList="getDataList"></add-or-update>
    </div>
</template>

<script>
import AddOrUpdate from './project-add-or-update';
export default {
    data() {
        return {
            dataForm: {
                projectType: null,
                projectSn: '',
                abbreviation: '',
            },
            brandId: 1,
            dataList: [],
            pageIndex: 1,
            pageSize: 10,
            totalPage: 0,
            dataListLoading: false,
            dataListSelections: [],
            addOrUpdateVisible: false,
            projectTypelist: [{value: null, label: '全部'}, {value: 1, label: '产业园'}, {value: 2, label: '住宅'}, {value: 3, label: '综合体'}],
        };
    },
    components: {
        AddOrUpdate,
    },
    activated() {
        this.getDataList();
    },
    // computed:{
    //   projectTypeCom:function(val){
    //       switch(val){
    //           case 1:
    //             return '产业园'
    //           break;
    //           case 2:
    //             return '住宅'
    //           break;
    //           case 1:
    //             return "综合体"
    //           break;
    //       }
    //   }
    // },
    methods: {
        // 获取数据列表
        getDataList() {
            this.dataListLoading = true;
            this.$http({
                url: this.$http.adornUrl('/v1/baseProject'),
                method: 'get',
                params: this.$http.adornParams({
                    pageNum: this.pageIndex,
                    pageSize: this.pageSize,
                    projectType: this.dataForm.projectType,
                    projectSn: this.dataForm.projectSn,
                    abbreviation: this.dataForm.abbreviation,
                    brandId: this.brandId,
                    // 'username': this.dataForm.userName
                }),
            }).then(({data}) => {
                if (data && data.code === 200) {
                    this.dataList = data.data.list;
                    this.totalPage = data.data.total;
                } else {
                    this.dataList = [];
                    this.totalPage = 0;
                }
                this.dataListLoading = false;
            });
        },
        // 每页数
        sizeChangeHandle(val) {
            this.pageSize = val;
            this.pageIndex = 1;
            this.getDataList();
        },
        // 当前页
        currentChangeHandle(val) {
            this.pageIndex = val;
            this.getDataList();
        },
        // 多选
        selectionChangeHandle(val) {
            this.dataListSelections = val;
        },
        // 新增 / 修改
        addOrUpdateHandle(id) {
            this.addOrUpdateVisible = true;
            this.$nextTick(() => {
                this.$refs.addOrUpdate.init(id);
            });
        },
        // 删除
        deleteHandle(id) {
            var projectId = id
                ? [id]
                : this.dataListSelections.map(item => {
                      return item.projectId;
                  });
            this.$confirm(`确定对[id=${projectId.join(',')}]进行[${id ? '删除' : '批量删除'}]操作?`, '提示', {
                confirmButtonText: '确定',
                cancelButtonText: '取消',
                type: 'warning',
            })
                .then(() => {
                    this.$http({
                        url: this.$http.adornUrl('/v1/baseProject/' + projectId),
                        method: 'delete',
                        // data: this.$http.adornData(projectId, false)
                    }).then(({data}) => {
                        if (data && data.code === 200) {
                            this.$message({
                                message: '操作成功',
                                type: 'success',
                                duration: 1500,
                                onClose: () => {
                                    this.getDataList();
                                },
                            });
                        }
                    });
                })
                .catch(() => {});
        },
        // 后端返回的数字，转换成中文
        formatType(row, column) {
            switch (row.projectType) {
                case 1:
                    return '产业园';
                    break;
                case 2:
                    return '住宅';
                    break;
                case 3:
                    return '综合体';
                    break;
            }
        },
    },
};
</script>
